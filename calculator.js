
// multiplying two values
function multiply(a,b) { 
    return a * b
}

// dividing one value by the other
function divide(a,b) { 
    return a / b
}

// summing two values
function add(a,b) { 
    return a + b
}

// subtracting two values
function subtract(a,b) { 
    return a - b
}

// running some examples
console.log("I'm going use the calculator functions to multiply 5 and 6")
var x = multiply(5,6)
console.log(x)

console.log("I'm going use the calculator functions to add 5 and 6")
var x = add(5,6)
console.log(x)

console.log("I'm going use the calculator functions to subtract 5 from 6")
var x = subtract(6,5)
console.log(x)

console.log("I'm going use the calculator functions to divide 6 from 5")
var x = divide(6,5)
console.log(x)



